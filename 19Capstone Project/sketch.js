function renderGrid(){
  for(let x = 0; x < numCols; x++){
    for(let y = 0; y < numRows; y++){
      if (mapData[y][x] === 0){
        fill(70,70,90);
        rect(x*tile, y*tile, tile, tile);
      }
      if (mapData[y][x] === 1){
        fill(255);
        rect(x*tile, y*tile, tile, tile);
      }
      if (mapData[y][x] === 2){
        fill(255,0,0);
        rect(x*tile, y*tile, tile, tile);
      }
    }
  }
}

let tile = 10;
let myPlayer;
let xPos;
let yPos;
const numCols = 40;
const numRows = 50;

function setup() {
  createCanvas(400, 500);
  strokeWeight(0.5);
  myPlayer = new player(25,24);
}

function draw() {
  renderGrid();
  grid();
  move();
  xPos = myPlayer.findX();
  yPos = myPlayer.findY();
  myPlayer.update();
  print(xPos,yPos);

}

function move(){
  if (keyIsPressed){
    //move right
    if (keyIsDown(68)){// 68 = d
      myPlayer.right();
    }
    //move left
    if (keyIsDown(65)){// 65 is a
      myPlayer.left();
    }
    //move down
    if (keyIsDown(83)){//83 is s
      myPlayer.down();
    }
    //move up
    if (keyIsDown(87)){//87 is w
      myPlayer.up();
    }
  }
}
// dashes
function keyPressed(){
  if (keyCode === SHIFT){
    if (keyIsDown(68)){
      myPlayer.dashRight();
    }
    if (keyIsDown(65)){
      myPlayer.dashLeft();
    }
    if (keyIsDown(83)){
      myPlayer.dashDown();
    }
    if (keyIsDown(87)){
      myPlayer.dashUp();
    }
  }
}

class block{
  constructor(x,y){
    this.x = x;
    this.y = y;
    this.tile = tile;
  }

  check(){
    fill(255);
    rect(this.x,this.y,this.tile);
  }
}

class player{
  constructor(x,y){
    this.x = x;
    this.y = y;
    this.tile = tile;
    this.speed = 2;
    this.dash = 25;
  }
  // always run update
  update(){
    fill(210,70,210);
    circle(this.x,this.y,tile);
  }
  //movement binds
  right(){
    if (this.x < 490){
      if(mapData[yPos][xPos] !== 1){
        this.x += this.speed;
      }
    }
  }

  dashRight(){
    if (this.x < 470){
      this.x += this.dash;
    }

  }

  left(){
    if (this.x > 0){
      if (mapData[yPos][xPos]){
        this.x -= this.speed;
      }
    }
  }

  dashLeft(){
    if (this.x > 20){
      if (mapData[yPos][xPos-2] !== 1){
        this.x -= this.dash;
      }
    }
  }

  down(){
    if (this.y < 390){
      if (mapData[yPos][xPos-1] !== 1){
        this.y += this.speed;
      }
    }
  }

  dashDown(){
    if (this.y < 370){
      if (mapData[yPos+2][xPos] !== 1){
        this.y += this.speed*10;
      }
    }
  }

  up(){
    if (this.y > 0){
      if (mapData[yPos-1][xPos-1] !== 1){
        this.y -= this.speed;
      }
    }
  }

  dashUp(){
    if (this.y > 20){
      if (mapData[yPos-2][xPos] !== 1){
        this.y -= this.speed*10;
      }
    }
  }

  // find values
  findX(){
    return round(this.x/tile);
  }

  findY(){
    return round(this.y/tile);
  }
}

function grid(){
  //vertical
  for(let i = 0; i < width; i += tile){
    line(i,0,i,height);
  }
  //horizontal
  for(let i = 0; i < height; i += tile){
    line(0,i,width,i);
  }
}


